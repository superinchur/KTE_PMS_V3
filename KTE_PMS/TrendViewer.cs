                {
                    Battery_DataSource.Fill();
                    POWER_ACCU_POWER.Fill();

                }
                else if (current_display == 1)
                {
                    Grid_DataSource.Fill();
                    POWER_ACCU_POWER.Fill();
                }
                else if (current_display == 2)
                {
                    Load_DataSource.Fill();
                }



