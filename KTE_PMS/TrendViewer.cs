                {
                    // AugoGenerating
                    Random rand = new Random();

                    Repository.Instance.dbConnector.Insert_BSC_Value_to_Database("trend_data", rand.Next(800, 1000) / 10, rand.Next(100, 200) / 10, rand.Next(400, 500) / 10);
